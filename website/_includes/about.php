	<div id="about-col">
		<h1 class="bio-head">Brooke Harris, MA, LPC, NCC, RPT</h1>
		<p>My name is Brooke Harris, the owner of Creative Counseling of Evergreen. I am a licensed professional counselor (LPC) in the state of Colorado as well as a national certified counselor (NCC). I am proud to be a registered play therapist (RPT) with the Association for Play Therapy. I graduated with a M.A. in Counseling from Western New Mexico University and I love what I do.</p> 

		<p>Although I have worked in different settings, geographical locations, and with
		different ages and cultures; my passion has always been practicing play therapy.
		When I work with teens and adults, I bring the spirit of play therapy in to those
		sessions as well. I view being a play therapist as a way of existing and interacting in the world. In congruence with play therapy, I believe sincerely in meeting people where they are and I genuinely view people with unconditional positive regard. I believe that therapy can be a collaborative and creative experience.</p>

		<p>In addition to counseling people of all ages during my career, I have taught parenting courses, stress management and anger management courses. I enjoy offering parenting support and guidance as part of the counseling process if requested. No matter who I work with, I view myself as a reflective tool, so the client may gain insight in to experiences, behaviors, thoughts and emotions.</p>

		<p>I was initially trained extensively in child centered, experiential play therapy, but I continue to educate myself and attend regular trainings in other modalities for toddlers, children, teens and adults. I have worked in a variety of settings: at a child development center in New Mexico, a mental health agency in Georgia and now at my own practice, Creative Counseling of Evergreen in beautiful Evergreen, Colorado.</p>

		<p>In my time away from work, I enjoy trail running with my rescue dogs, hiking,
		traveling, trying new restaurants, and spending time with my family. I also enjoy volunteering for the Colorado Association of Play Therapy (CAPT) as a member of the board of directors. I live in the mountains near Evergreen with my husband and two young children.</p>
		
		<p>If you think that my experience and approach can be helpful to you or someone you care about, please reach out to me. I am glad to help! I offer a free 30 minute phone consultation prior to the first office session.</p>
